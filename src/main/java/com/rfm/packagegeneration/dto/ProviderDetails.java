package com.rfm.packagegeneration.dto;

public class ProviderDetails {
	private String providerTerminal;
	private String providerMail;
	private String providerAlias;
	private Long providerLexId;
	private Long providerId;
	private String srceWd;
	public String getProviderTerminal() {
		return providerTerminal;
	}
	public void setProviderTerminal(String providerTerminal) {
		this.providerTerminal = providerTerminal;
	}
	public String getProviderMail() {
		return providerMail;
	}
	public void setProviderMail(String providerMail) {
		this.providerMail = providerMail;
	}
	public String getProviderAlias() {
		return providerAlias;
	}
	public void setProviderAlias(String providerAlias) {
		this.providerAlias = providerAlias;
	}
	public Long getProviderLexId() {
		return providerLexId;
	}
	public void setProviderLexId(Long providerLexId) {
		this.providerLexId = providerLexId;
	}
	public Long getProviderId() {
		return providerId;
	}
	public void setProviderId(Long providerId) {
		this.providerId = providerId;
	}
	public String getSrceWd() {
		return srceWd;
	}
	public void setSrceWd(String srceWd) {
		this.srceWd = srceWd;
	}
	
	

}
