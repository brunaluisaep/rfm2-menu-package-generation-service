package com.rfm.packagegeneration.dto;

public class ProductAbsSettings {
	private String cupSize;
	private Long flavor;
	private String lidOption;
	private String iceSelection;
	private String iceProductDef;
	private Long ngabsVol;
	
	public String getCupSize() {
		return cupSize;
	}
	public void setCupSize(String cupSize) {
		this.cupSize = cupSize;
	}
	public Long getFlavor() {
		return flavor;
	}
	public void setFlavor(Long flavor) {
		this.flavor = flavor;
	}
	public String getLidOption() {
		return lidOption;
	}
	public void setLidOption(String lidOption) {
		this.lidOption = lidOption;
	}
	public String getIceSelection() {
		return iceSelection;
	}
	public void setIceSelection(String iceSelection) {
		this.iceSelection = iceSelection;
	}
	public String getIceProductDef() {
		return iceProductDef;
	}
	public void setIceProductDef(String iceProductDef) {
		this.iceProductDef = iceProductDef;
	}
	public Long getNgabsVol() {
		return ngabsVol;
	}
	public void setNgabsVol(Long ngabsVol) {
		this.ngabsVol = ngabsVol;
	}
		

}
